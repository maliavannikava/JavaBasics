package com.class07;

public class DoWhileLoop {

	public static void main(String[] args) {
		
		//while loop
		int num=11;
		while(num<=5) {
			System.out.println("Hello");
			num++;
		}
		
		
		//do wile loop
		int num1=11;
		do {
			System.out.println("Bye");
			num1++;
		}while (num<=5);

	}

}
