package com.class02;

public class ModulusOperator {

	public static void main(String[] args) {
		
		float f=12.5f;
		float f1=2.7f;
		
		float result=f/f1;
		System.out.println(result);
		
		int i=12;
		int y=7;
		int result1=i/y;
		System.out.println(result1);
		
		
		int a=16;
		int b=5;
		
		int mod=a%b;
		System.out.println(mod);
		
		
	}
}
